interface AppSidebarProps {
  onClose?: () => void;
}

export function AppSidebar({ onClose }: AppSidebarProps) {
  const pathname = usePathname();
  const { user } = useAuth();
  const { isAdmin, isGlobalAdmin } = useRole();

  const filteredItems = navItems.filter((item) => {
    if (item.globalAdminOnly && !isGlobalAdmin) return false;
    if (item.adminOnly && !isAdmin) return false;
    return true;
  });

  return (
    <div className="flex h-full flex-col bg-white dark:bg-zinc-900 border-r border-zinc-200 dark:border-zinc-700">
      {/* Logo */}
      <div className="flex h-16 items-center gap-3 px-6 border-b border-zinc-200 dark:border-zinc-700">
        <div className="flex h-8 w-8 items-center justify-center rounded-lg bg-zinc-900 dark:bg-white">
          <span className="text-sm font-bold text-white dark:text-zinc-900">FM</span>
        </div>
        <span className="text-lg font-bold text-zinc-900 dark:text-white">
          Field Manager
        </span>
      </div>

      {/* Navigation */}
      <nav className="flex-1 overflow-y-auto px-3 py-4">
        <ul className="space-y-1">
          {filteredItems.map((item) => {
            const isActive =
              pathname === item.href || pathname.startsWith(item.href + "/");
            return (
              <li key={item.href}>
                <Link
                  href={item.href}
                  onClick={onClose}
                  className={clsx(
                    "flex items-center gap-3 rounded-lg px-3 py-2 text-sm font-medium transition-colors",
                    isActive
                      ? "bg-zinc-100 text-zinc-900 dark:bg-zinc-800 dark:text-white"
                      : "text-zinc-600 hover:bg-zinc-50 hover:text-zinc-900 dark:text-zinc-400 dark:hover:bg-zinc-800 dark:hover:text-white"
                  )}
                >
                  {item.icon}
                  {item.label}
                </Link>
              </li>
            );
          })}
        </ul>
      </nav>

      {/* User section */}
      <div className="border-t border-zinc-200 dark:border-zinc-700 p-4">
        <div className="flex items-center gap-3">
          <div className="flex h-8 w-8 items-center justify-center rounded-full bg-zinc-200 dark:bg-zinc-700">
            <span className="text-xs font-medium text-zinc-600 dark:text-zinc-300">
              {user?.displayName?.[0]?.toUpperCase() || user?.email?.[0]?.toUpperCase() || "?"}
            </span>
          </div>
          <div className="flex-1 min-w-0">
            <p className="truncate text-sm font-medium text-zinc-900 dark:text-white">
              {user?.displayName || user?.email}
            </p>
            <p className="truncate text-xs text-zinc-500 dark:text-zinc-400 capitalize">
              {user?.role?.replace("_", " ")}
            </p>
          </div>
          <button
            onClick={() => signOut()}
            className="rounded-lg p-1.5 text-zinc-400 hover:bg-zinc-100 hover:text-zinc-600 dark:hover:bg-zinc-800 dark:hover:text-zinc-300 transition-colors"
            title="Sign out"
          >
            <LogOutIcon />
          </button>
        </div>
      </div>
    </div>
  );
}
